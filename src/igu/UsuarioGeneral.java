        private void cargarTabla() {
        DefaultTableModel modeloTabla = new DefaultTableModel(){
            @Override
            public boolean isCellEditable(int row, int colum){
                return false;
            }
        };
        String titulos[] = {"Id", "Nombre", "Apellido", "Usuario", "Rol"};
        modeloTabla.setColumnIdentifiers(titulos);
        ArrayList<Usuario> listaUsuarios = new ArrayList<Usuario>();
        listaUsuarios = ctrlLogica.traerUsuarios();
        if(listaUsuarios != null){
            for(Usuario user : listaUsuarios){
                Object objetoUsuario[] = {user.getId(), user.getNombre(), user.getApellido(), user.getUsuario(), user.getRol()};
                modeloTabla.addRow(objetoUsuario);
            }
        }else{
            mostrarMensaje("No existe ningun elemento en la base de datos", "Error", "Base de datos vacia");
        }
        
        tablaUsuarios.setModel(modeloTabla);
    }
    
    public void mostrarMensaje(String mensaje, String tipo, String titulo){
        JOptionPane optionPane = new JOptionPane(mensaje);
        if(tipo.equals("Info")){
            optionPane.setMessageType(JOptionPane.INFORMATION_MESSAGE);
        }
        else if(tipo.equals("Error")){
            optionPane.setMessageType(JOptionPane.ERROR_MESSAGE);
        }
        JDialog dialog = optionPane.createDialog(titulo);
        dialog.setAlwaysOnTop(true);
        dialog.setVisible(true);
    }
